        context,
      ).showSnackBar(const SnackBar(content: Text('M·∫≠t kh·∫©u kh√¥ng kh·ªõp')));
      return;
    }

    setState(() => _loading = true);

    try {
      await FirebaseAuth.instance.createUserWithEmailAndPassword(
        email: email,
        password: password,
      );

      ScaffoldMessenger.of(
        context,
      ).showSnackBar(const SnackBar(content: Text('ƒêƒÉng k√Ω th√†nh c√¥ng üéâ')));

      if (mounted) {
        Navigator.pushReplacement(
          context,
          MaterialPageRoute(builder: (_) => const LoginPage()),
        );
      }
    } on FirebaseAuthException catch (e) {
      String message = 'ƒêƒÉng k√Ω th·∫•t b·∫°i';
      if (e.code == 'email-already-in-use') {
        message = 'Email ƒë√£ ƒë∆∞·ª£c s·ª≠ d·ª•ng';
      } else if (e.code == 'invalid-email') {
        message = 'Email kh√¥ng h·ª£p l·ªá';
      } else if (e.code == 'weak-password') {
        message = 'M·∫≠t kh·∫©u qu√° y·∫øu (√≠t nh·∫•t 6 k√Ω t·ª±)';
      }
      ScaffoldMessenger.of(
        context,
      ).showSnackBar(SnackBar(content: Text(message)));
    } finally {
      if (mounted) setState(() => _loading = false);
    }
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      backgroundColor: RetroTheme.beige,
      body: Center(
        child: SingleChildScrollView(
          padding: const EdgeInsets.all(24),
          child: Container(
            padding: const EdgeInsets.all(24),
            decoration: BoxDecoration(
              color: Colors.white,
              borderRadius: BorderRadius.circular(16),
